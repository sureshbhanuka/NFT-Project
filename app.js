async function connectWallet() {
    if (window.ethereum) {
        try {
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            const accounts = await window.ethereum.request({ method: 'eth_accounts' });
            document.getElementById('status').innerText = `Connected: ${accounts[0]}`;
            document.getElementById('mintNFT').disabled = false;
            updateMintingProgress();
            loadNFTs(accounts[0]);
        } catch (error) {
            console.error("User denied account access", error);
            document.getElementById('status').innerText = 'Connection failed!';
        }
    } else {
        alert('Please install MetaMask!');
    }
}

async function mintNFT() {
    const web3 = new Web3(window.ethereum);
    const accounts = await web3.eth.getAccounts();
    const contract = new web3.eth.Contract(contractABI, contractAddress);

    try {
        const result = await contract.methods.PublicMint().send({ from: accounts[0], value: web3.utils.toWei('0.001', 'ether') });
        document.getElementById('status').innerText = `Minted! Transaction hash: ${result.transactionHash}`;
        updateMintingProgress();
        loadNFTs(accounts[0]);
    } catch (error) {
        console.error("Minting failed", error);
        document.getElementById('status').innerText = 'Minting failed!';
    }
}

async function loadNFTs(account) {
    const web3 = new Web3(window.ethereum);
    const contract = new web3.eth.Contract(contractABI, contractAddress);

    try {
        const balance = await contract.methods.balanceOf(account).call();
        const nftContainer = document.getElementById('nftContainer');
        nftContainer.innerHTML = '';

        for (let i = 0; i < balance; i++) {
            const tokenId = await contract.methods.tokenOfOwnerByIndex(account, i).call();
            const tokenURI = await contract.methods.tokenURI(tokenId).call();

            const ipfsGatewayUrl = tokenURI.replace('ipfs://', 'https://ipfs.io/ipfs/');

            try {
                const response = await fetch(ipfsGatewayUrl);
                if (!response.ok) {
                    throw new Error(`HTTP error! status: ${response.status}`);
                }
                const metadata = await response.json();

                const nftElement = document.createElement('div');
                nftElement.className = 'nft';
                nftElement.innerHTML = `
                    <a href="https://testnets.opensea.io/assets/sepolia/${contractAddress}/${tokenId}" target="_blank">
                        <img src="${metadata.image.replace('ipfs://', 'https://ipfs.io/ipfs/')}" alt="NFT Image" onerror="this.onerror=null;this.src='placeholder.png';">
                        <p>Token ID: ${tokenId}</p>
                        <p>Name: ${metadata.name}</p>
                        <p>Description: ${metadata.description}</p>
                    </a>
                `;
                nftContainer.appendChild(nftElement);
            } catch (err) {
                console.error(`Failed to fetch metadata for Token ID ${tokenId}:`, err);
                const nftElement = document.createElement('div');
                nftElement.className = 'nft';
                nftElement.innerHTML = `
                    <p>Token ID: ${tokenId}</p>
                    <p>Error loading metadata</p>
                `;
                nftContainer.appendChild(nftElement);
            }
        }
    } catch (error) {
        console.error("Failed to load NFTs", error);
    }
}

async function updateMintingProgress() {
    const web3 = new Web3(window.ethereum);
    const contract = new web3.eth.Contract(contractABI, contractAddress);

    try {
        const totalMinted = await contract.methods.totalMinted().call();
        document.getElementById('mintingProgress').innerText = `${totalMinted}/${maxSupply}`;
    } catch (error) {
        console.error("Failed to fetch minting progress", error);
    }
}

